package com.ontech.com.abgec.Model;

public class user_dataModel {

    private String bio;
    private String branch;
    private String city;
    private String country;
    private String designation;
    private String dob;
    private String dp_link;
    private String fb;
    private String gender;
    private String id;
    private String insta;
    private String linkedin;
    private String name;
    private String occupation;
    private String passout;
    private String phone;
    private String state;
    private String token;
    private String twitter;
    private String uid;

    public user_dataModel() {
    }

    public user_dataModel(String bio, String branch, String city, String country, String designation, String dob, String dp_link, String fb, String gender, String id, String insta, String linkedin, String name, String occupation, String passout, String phone, String state, String token, String twitter, String uid) {
        this.bio = bio;
        this.branch = branch;
        this.city = city;
        this.country = country;
        this.designation = designation;
        this.dob = dob;
        this.dp_link = dp_link;
        this.fb = fb;
        this.gender = gender;
        this.id = id;
        this.insta = insta;
        this.linkedin = linkedin;
        this.name = name;
        this.occupation = occupation;
        this.passout = passout;
        this.phone = phone;
        this.state = state;
        this.token = token;
        this.twitter = twitter;
        this.uid = uid;
    }

    public String getBio() {
        return bio;
    }

    public String getBranch() {
        return branch;
    }

    public String getCity() {
        return city;
    }

    public String getCountry() {
        return country;
    }

    public String getDesignation() {
        return designation;
    }

    public String getDob() {
        return dob;
    }

    public String getDp_link() {
        return dp_link;
    }

    public String getFb() {
        return fb;
    }

    public String getGender() {
        return gender;
    }

    public String getId() {
        return id;
    }

    public String getInsta() {
        return insta;
    }

    public String getLinkedin() {
        return linkedin;
    }

    public String getName() {
        return name;
    }

    public String getOccupation() {
        return occupation;
    }

    public String getPassout() {
        return passout;
    }

    public String getPhone() {
        return phone;
    }

    public String getState() {
        return state;
    }

    public String getToken() {
        return token;
    }

    public String getTwitter() {
        return twitter;
    }

    public String getUid() {
        return uid;
    }
}
